#include "ft_calculator.h"

int		ft_div(int n1, int n2)
{
	return (n1 / n2);
}

int		ft_mult(int n1, int n2)
{
	return (n1 * n2);
}

int		ft_add(int n1, int n2)
{
	return (n1 + n2);
}

int		ft_substr(int n1, int n2)
{
	return (n1 - n2);
}

int		ft_mod(int n1, int n2)
{
	return (n1 % n2);
}
